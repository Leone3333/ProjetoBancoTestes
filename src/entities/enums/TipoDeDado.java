package entities.enums;

/**
 * Enumeração que representa os tipos de dados disponíveis.
 */
public enum TipoDeDado {

    EMAIL, // Email da empresa ou do titular.
    IDENTIFICACAO, // CNPJ da empresa ou CPF do titular.
    NOME, // Nome da empresa ou do titular.
    DATA, // Data de criação da empresa ou data de nascimento do titular.
    NUMERODACONTA, // Número da empresa ou do titular.
    TIPODACONTA, // Tipo da conta (pessoa física ou pessoa jurídica).
    SALDO, // Saldo da conta.
    ENDERECODEEMAIL, // Endereço de email da empresa ou do titular.
    SENHA; // Senha da conta.

}
